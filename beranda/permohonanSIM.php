                        diwilayah wajib memiliki Surat Ijin Mengemudi (SIM).
                        <br><br>
                        <b>Penggunaan Golongan SIM
                            Pasal 211 (2) PP 44 / 93</b>
                        <br><br>
                        <b>Golongan A</b><br>
                        untuk mengemudikan mobil penumpang, mobil bus dan mobil barang yang mempunyai jumlah berat
                        yang diperbolehkan tidak lebih dari 3.500 kilogram;
                        <br><br>
                        <b>Golongan B I</b><button></button>
                        untuk mengemudikan mobil bus dan mobil barang yang mempunyai jumlah berat yang diperbolehkan
                        lebih dari 3.500 kilogram;
                        <br><br>
                        <b>Golongan B I1</b><br>
                        untuk mengemudikan tractor atau kendaraan bermotor dengan menarik kereta tempelan atau gandengan
                        berat yang diperbolehkan untuk kereta tempelan atau kereta gandengan lebih dari 1.000 kilogram;
                        <br><br>
                        <b>Golongan C</b><br>
                        untuk mengemudikan sepeda motor yang dirancang mampu mencapai kecepatan lebih dari 40
                        kilometer per jam;
                        <br><br>
                        <b>Golongan D</b><br>
                        untuk mengemudikan sepeda motor yang dirancang dengan kecepatan tidak lebih dari 40 kilometer
                        per jam.
