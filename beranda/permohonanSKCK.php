            <p>
                Surat Keterangan Catatan Kepolisian atau biasa disingkat dengan SKCK adalah surat keterangan resmi yang
                diterbitkan oleh Polri melalui fungsi Intelkam kepada seorang pemohon/warga masyarakat untuk menerangkan
                tentang ada ataupun tidak adanya catatan suatu individu atau seseorang yang bersangkutan dalam kegiatan
                kriminalitas atau kejahatan. Masa berlaku SKCK hingga 6 (enam) bulan sejak tanggal diterbitkan. Jika
                telah melewati masa berlaku dan bila dirasa perlu, SKCK dapat diperpanjang.
                <br><br>
                Tata cara permohonan untuk memperoleh SKCK dapat dilakukan dengan cara mendaftar secara langsung di
                loket pelayanan SKCK di setiap kantor polisi dengan membawa dokumen yang dipersyaratkan serta mengisi
                formulir yang telah disiapkan oleh petugas, atau mendaftar secara online dengan cara mengunggah dokumen
