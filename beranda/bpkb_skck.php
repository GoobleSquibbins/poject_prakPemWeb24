                <h1 id="layanan">
                    Layanan Permohonan Pengawalan Jalan
                </h1>

                <div class="card" id="kawal">
                    <p class="secTitle">Deskripsi</p>
                    <div class="cardTxt">
                        Pada dasarnya menggunakan sarana dan prasana jalan untuk keperluan berlalu lintas adalah hak
                        asasi setiap orang. Semua orang mempunyai hak yang sama untuk menggunakan jalan untuk berlalu
                        lintas. Tidak ada seorang pun mempunyai hak untuk diutamakan, kecuali didasarkan pada peraturan
                        perundang-undangan yang berlaku. Peraturan perundang-undangan yang ada memberikan peluang bagi
                        orang tertentu atau kendaraan yang digunakan bagi keperluan tertentu mendapatkan prioritas
                        menggunakan jalan untuk berlalu lintas. Hak utama itu diatur dalam Peraturan Pemerintah (PP)
                        Nomor 43 Tahun 1993.
                        <br><br>
                        Dalam Pasal 65 ayat 1 disebutkan, pemakai jalan wajib mendahulukan sesuai urutan prioritas
                        sebagai berikut:
                        <ol>
                            <li>kendaraan pemadam kebakaran yang sedang melaksanakan tugas</li>
                            <li>ambulans yang mengangkut orang sakit</li>
                            <li>kendaraan untuk memberi pertolongan pada kecelakaan lalu lintas</li>
                            <li>kendaraan kepala negara (Presiden dan Wakil Presiden) atau pemerintah asing yang menjadi
                                tamu negara</li>
                            <li>iring-iringan pengantar jenazah</li>
                            <li>konvoi, pawai atau kendaraan orang cacat</li>
                            <li>kendaraan yang penggunaannya untuk keperluan khusus atau mengangkut barang-barang khusus
                            </li>
                        </ol>
                        Semua kendaraan tersebut di atas wajib didahulukan dalam berlalu lintas. Kendaraan yang
                        mendapatkan prioritas tersebut, berdasarkan ayat 2 Pasal 65 PP diatas harus disertai dengan
                        peng-awalan petugas yang berwenang atau dilengkapi dengan isyarat atau tanda-tanda lain.
                        Dalam ayat 3 ditegaskan lagi, petugas yang berwenang melakukan pengamanan apabila mengetahui
                        adanya pemakai jalan yang diprioritaskan tersebut.
                        Dalam ayat 4 ditambahkan, perintah atau larangan yang dinyatakan dengan alat pemberi isyarat
                        lalu-lintas tentang isyarat berhenti tidak diberlakukan kepada kendaraan-kendaraan sebagaimana
                        dimaksud dalam ayat 1 huruf “a” sampai dengan “e”.
                    </div>
                </div>

                <div class="idk">
                    <div class="card">
                        <p class="secTitle">Kewenangan Pengawalan Jalan oleh Polri</p>
                        <div class="cardTxt">
                            Esensi dari pengawalan tidak lain memang memberikan pengamanan, baik terhadap kendaraan yang
                            dikawal, maupun pengguna jalan lain yang berada di sekitar kendaraan yang dikawal. Karena
                            menyangkut pengamanan, pihak yang paling berwenang adalah POLRI. Karena pengamanan adalah
                            bagian dari tugas pokok Polri.
                            <br><br>
                            Dalam <b>Pasal 14 ayat 1 huruf “a” UU Nomor 2 tahun 2002</b> tentang Kepolisian Negara
                            Republik
                            Indonesia disebutkan, dalam melaksanakan tugas pokoknya, Polri bertugas melaksanakan
                            pengaturan, penjagaan, pengawalan, dan patroli terhadap kegiatan masyarakat dan pemerintah
                            sesuai kebutuhan.
                        </div>
                    </div>
                    <div class="card">
                        <p class="secTitle">Konsekuensi Pengguna Jalan Lainnya</p>
                        <div class="cardTxt">
                            Bila terdapat tindakan pengawalan jalan, maka menjadi kewajiban kepada pengguna jalan lain
                            untuk memberikan prioritas kepada kendaraan yang dikawal.
                            <br><br>
                            Dalam Pasal 34 ayat 1 PP Nomor 43 Tahun 1993 ditegaskan bahwa dalam keadaan tertentu petugas
                            Kepolisian Negara Republik Indonesia dapat melakukan tindakan:
                            <ol>
                                <li>memberhentikan arus lalu lintas dan/atau pemakai jalan tertentu</li>
                                <li>memerintahkan pemakai jalan untuk jalan terus</li>
                                <li>mempercepat arus lalu lintas</li>
                                <li>memperlambat arus lalu lintas</li>
                                <li>mengubah arah arus lalu lintas</li>
                            </ol>
                            Dalam Pasal 34 Ayat 2 PP Nomor 43 Tahun 1993 juga ditekankan, pemakai jalan wajib mematuhi
                            perintah yang diberikan oleh petugas polisi. Pada ayat 2 dipertegas lagi, perintah yang
                            diberikan oleh petugas polisi sebagaimana dimaksud dalam ayat 1, wajib didahulukan daripada
                            perintah yang diberikan oleh alat pemberi isyarat lalu lintas.
                        </div>
